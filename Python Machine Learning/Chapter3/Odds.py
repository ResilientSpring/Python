# In the theory of probability and statistics, a Bernoulli trial (or binomial trial) is
# a random experiment with exactly two possible outcomes, "success" and "failure".
# Since a Bernoulli trial has only two possible outcomes, it can be framed as some
# "yes or no" question.

# Rolling a die, where a six is "success" and everything else a "failure".
# In this case there are six possible outcomes, and the event is a six;
# the complementary event "not a six" corresponds to the other five possible outcomes.

# Independent repeated trials of an experiment with exactly two possible outcomes
# are called Bernoulli trials.
# Call one of the outcomes "success" and the other outcome "failure".
# Let p be the probability of success in a Bernoulli trial,
# and q be the probability of failure.
# Then the probability of success and the probability of failure sum to one,
# since these are complementary events: "success" and "failure" are mutually exclusive
# and exhaustive.

# Thus one has the following relations:
#   p + q = 1
#   q = 1 - p
#   p = 1 - q

# Example:
#   There are 5 pink marbles, 2 blue marbles, and 8 purple marbles.
#   What are the odds in favor of picking a blue marble?
#       Answer: The odds in favour of a blue marble are 2:13.
#       One can equivalently say, that the odds are 13:2 against.
#       There are 2 out of 15 chances in favour of blue, 13 out of 15 against blue.


