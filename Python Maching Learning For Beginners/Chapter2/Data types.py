# A string Variable
first_name = "Joseph";
print(type(first_name));

# An integer variable
age = 20;
print(type(age));

# A floating point variable
weight = 70.35;
print(type(weight));

# A boolean variable
married = False;
print(type(married));

# List
cars = ["Honda", "Toyota", "Suzuki"];
print(type(cars));

# Tuples / a structure of data that has several parts.
# Reference: https://dictionary.cambridge.org/dictionary/english/tuple
days = ("Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday");
print(type(days));

# Dictionaries
days2 = {1: "Sunday", 2: "Monday", 3: "Tuesday", 4: "Wednesday", 5: "Thursday",
         6: "Friday", 7: "Saturday"};
print(type(days2));
