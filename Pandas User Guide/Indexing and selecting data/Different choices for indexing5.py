# The Python and NumPy indexing operators [] and attribute operator .
# provide quick and easy access to pandas data structures across a wide range of use cases.

# This makes interactive work intuitive, as there’s little new to learn if you already know
# how to deal with Python dictionaries and NumPy arrays.

# However, since the type of the data to be accessed isn’t known in advance,
# directly using standard operators has some optimization limits.

# For production code, we recommended that you take advantage of
# the optimized pandas data access methods exposed in this chapter.

