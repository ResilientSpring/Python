import numpy as np

array_example = np.array([[[0, 1, 2, 3], [4, 5, 6, 7]],
                          [[0, 1, 2, 3], [4, 5, 6, 7]],
                          [[0, 1, 2, 3], [4, 5, 6, 7]]]);

# Size will tell you the total number of elements of the array.
# This is a product of the elements of the array's shape.
print(array_example.size);

